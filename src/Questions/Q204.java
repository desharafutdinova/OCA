/*

Which two statements describe the object-oriented features of the Java language? (Choose three.)
A. Objects cannot be reused.
B. A subclass must override the methods from a superclass.
C. Objects can share behaviors with other objects.
D. A package must contain a main class.
E. Object is the root class of all other objects.
F. A main method must be declared in every class.

 */

/*

Explanation:
Objects cannot be reused - wrong, the object in java are reusable;
A subclass must override the methods from a superclass - wrong, the subclass has an ability to override, ut it not a must in regular classes. The case of "MUST" is only for abstract parent class - concrete class
Objects can share behaviors with other objects - true, sharing behaviour means sharing/using/implementing same methods
A package must contain a main class - wrong, it's not a "MUST' for a package to have main methods in any of the classes, ex: utils package, java lang etc.
Object is the root class of all other objects - true, Object is a main parent of all classes in java
A main method must be declared in every class - wrong, it only can appear once in the java file no matter how many classes this java file has. It can appear in each class if all of them are in the different files, but again not a "MUST".

 */

// Answer is C, E
